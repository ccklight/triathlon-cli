Project Title:
Triathlon Races by US Cities for the Novice.  Triathlons are swim-bike-run events of varying distances.  For the amateur,
Sprint distance triathlons are perfect for introduction to the multi-sport events.


Installation  At the command line in the terminal, type the following: ./bin/triathlon 

This is going to install through BASH looking at the interpreter to use rather than installing through the Ruby interpreter explicitly.
